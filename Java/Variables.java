public class Variables {
    public static void main(String[] args) {
        float gpa = 3.836f;
        int age = 45;
        String name = "Mike";
        
        // Note that the first word of the variable declaration is the type.
        
        System.out.println(name + " is " + age + " and wished he had a " + gpa + " gpa.");
    }
}